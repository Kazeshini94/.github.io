package helloworld;               // Variablen lernen  datentyp unterscheidet sich je nach art der variablen buchstaben, zahlen etc.

public class Program {

	public static void main(String[] args) {
		                  
		                   // Int = Datentyp  Age = Bezeichner 
		int age = 27;      //Initialisierung (wir weisen direkt eine variable als Basiswert zu)
		
		System.out.println(age);
		
		
		int age2;          //Deklaration  (bekommt intern einen Basiswert zugeteilt)
		age2 = 29;         //Zuweisung    (wir weisen Age2 erst jz eine Variable zu) beide methoden funktionieren

		System.out.println(age2);
		
		age2 = 30;
		
		System.out.println(age2);
		
	}

}
